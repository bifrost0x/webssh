const i18n = {
    currentLang: localStorage.getItem('language') || 'en',

    t(key) {
        return translations[this.currentLang][key] || translations['en'][key] || key;
    },

    setLanguage(lang) {
        if (translations[lang]) {
            this.currentLang = lang;
            localStorage.setItem('language', lang);
            this.updatePageText();

            window.dispatchEvent(new CustomEvent('languageChanged', { detail: { lang } }));
            return true;
        }
        return false;
    },

    getLanguage() {
        return this.currentLang;
    },

    getLanguages() {
        return [
            { code: 'en', name: 'English', flag: '🇬🇧' },
            { code: 'de', name: 'Deutsch', flag: '🇩🇪' },
            { code: 'fr', name: 'Français', flag: '🇫🇷' },
            { code: 'es', name: 'Español', flag: '🇪🇸' }
        ];
    },

    updatePageText() {
        document.querySelectorAll('[data-i18n]').forEach(element => {
            const key = element.getAttribute('data-i18n');
            const translation = this.t(key);

            if (element.tagName === 'INPUT' && element.type !== 'submit') {
                element.placeholder = translation;
            } else {
                element.textContent = translation;
            }
        });

        if (document.title.includes('SSH Terminal')) {
            document.title = this.t('app.title');
        }
    }
};

window.i18n = i18n;

const originalGetLanguages = i18n.getLanguages.bind(i18n);
const originalUpdatePageText = i18n.updatePageText.bind(i18n);

i18n.getLanguages = function getLanguagesWithChinese() {
    return [
        ...originalGetLanguages(),
        { code: 'zh', name: '中文', flag: 'ZH' }
    ];
};

i18n.updatePageText = function updatePageTextWithPlaceholderSupport() {
    originalUpdatePageText();

    document.querySelectorAll('[data-i18n-placeholder]').forEach(element => {
        const key = element.getAttribute('data-i18n-placeholder');
        element.placeholder = this.t(key);
    });
};

document.addEventListener('DOMContentLoaded', () => {
    i18n.updatePageText();
});
